CREATE TYPE "Payer" AS ENUM ('father', 'mother');
CREATE TYPE "ExpenseSource" AS ENUM ('rakuten', 'advance', 'personal');

CREATE TABLE "categories" (
  "id" text NOT NULL,
  "name" text NOT NULL,
  "is_active" boolean NOT NULL DEFAULT true,
  "sort_order" integer NOT NULL DEFAULT 0,
  "user_id" uuid,
  "created_at" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updated_at" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "categories_pkey" PRIMARY KEY ("id")
);

CREATE TABLE "sub_categories" (
  "id" text NOT NULL,
  "category_id" text NOT NULL,
  "name" text NOT NULL,
  "is_active" boolean NOT NULL DEFAULT true,
  "sort_order" integer NOT NULL DEFAULT 0,
  "created_at" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updated_at" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "sub_categories_pkey" PRIMARY KEY ("id")
);

CREATE TABLE "expenses" (
  "id" uuid NOT NULL DEFAULT gen_random_uuid(),
  "no" integer GENERATED BY DEFAULT AS IDENTITY,
  "accounting_month" varchar(7) NOT NULL,
  "date" date,
  "category_id" text NOT NULL,
  "sub_category_id" text NOT NULL,
  "item" text NOT NULL DEFAULT '',
  "description" text NOT NULL DEFAULT '',
  "amount" integer NOT NULL,
  "payer" "Payer" NOT NULL,
  "source" "ExpenseSource" NOT NULL,
  "reimbursed" boolean NOT NULL DEFAULT false,
  "beneficiary" text NOT NULL DEFAULT '',
  "comment" text NOT NULL DEFAULT '',
  "user_id" uuid,
  "created_at" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updated_at" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "expenses_pkey" PRIMARY KEY ("id")
);

CREATE TABLE "deposits" (
  "id" uuid NOT NULL DEFAULT gen_random_uuid(),
  "no" integer GENERATED BY DEFAULT AS IDENTITY,
  "date" date NOT NULL,
  "depositor" "Payer" NOT NULL,
  "amount" integer NOT NULL,
  "description" text NOT NULL DEFAULT '',
  "comment" text NOT NULL DEFAULT '',
  "user_id" uuid,
  "created_at" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "updated_at" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "deposits_pkey" PRIMARY KEY ("id")
);

CREATE INDEX "expenses_accounting_month_idx" ON "expenses"("accounting_month");
CREATE INDEX "expenses_category_id_idx" ON "expenses"("category_id");
CREATE INDEX "expenses_sub_category_id_idx" ON "expenses"("sub_category_id");
CREATE INDEX "expenses_payer_idx" ON "expenses"("payer");
CREATE INDEX "expenses_source_idx" ON "expenses"("source");
CREATE INDEX "deposits_date_idx" ON "deposits"("date");
CREATE INDEX "deposits_depositor_idx" ON "deposits"("depositor");
CREATE INDEX "categories_is_active_idx" ON "categories"("is_active");
CREATE INDEX "categories_sort_order_idx" ON "categories"("sort_order");
CREATE INDEX "sub_categories_category_id_idx" ON "sub_categories"("category_id");
CREATE INDEX "sub_categories_is_active_idx" ON "sub_categories"("is_active");
CREATE INDEX "sub_categories_sort_order_idx" ON "sub_categories"("sort_order");

ALTER TABLE "sub_categories" ADD CONSTRAINT "sub_categories_category_id_fkey" FOREIGN KEY ("category_id") REFERENCES "categories"("id") ON DELETE RESTRICT ON UPDATE CASCADE;
ALTER TABLE "expenses" ADD CONSTRAINT "expenses_category_id_fkey" FOREIGN KEY ("category_id") REFERENCES "categories"("id") ON DELETE RESTRICT ON UPDATE CASCADE;
ALTER TABLE "expenses" ADD CONSTRAINT "expenses_sub_category_id_fkey" FOREIGN KEY ("sub_category_id") REFERENCES "sub_categories"("id") ON DELETE RESTRICT ON UPDATE CASCADE;

